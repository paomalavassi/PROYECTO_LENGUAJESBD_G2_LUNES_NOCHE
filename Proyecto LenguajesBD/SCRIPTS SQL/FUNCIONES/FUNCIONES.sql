FUNCTION FIDE_TOTAL_FACTURAS_PERSONA_FN(P_ID_PERSONA NUMBER) RETURN NUMBER IS
V_TOTAL NUMBER := 0;
BEGIN
FOR REG IN (
SELECT COUNT(*) AS TOTAL
FROM FIDE_FACTURAS_TB
WHERE ID_PERSONA = P_ID_PERSONA
) LOOP
V_TOTAL := REG.TOTAL;
END LOOP;
RETURN V_TOTAL;
END FIDE_TOTAL_FACTURAS_PERSONA_FN;
 
FUNCTION FIDE_MONTO_FACTURADO_PERSONA_FN(P_ID_PERSONA NUMBER) RETURN NUMBER IS
V_TOTAL NUMBER := 0;
BEGIN
FOR REG IN (
SELECT COUNT(F.ID_FACTURA) AS TOTAL
FROM FIDE_FACTURAS_TB F
WHERE F.ID_PERSONA = P_ID_PERSONA
AND F.ID_ESTADO = 1
) LOOP
V_TOTAL := REG.TOTAL;
END LOOP;
RETURN V_TOTAL;
END FIDE_MONTO_FACTURADO_PERSONA_FN;
 
