FUNCTION FIDE_TOTAL_TURNOS_GUARDIA_FN(P_ID_PERSONA NUMBER) RETURN NUMBER IS
CURSOR_TURNOS SYS_REFCURSOR;
V_TOTAL NUMBER := 0;
BEGIN
OPEN CURSOR_TURNOS FOR
SELECT COUNT(*)
FROM FIDE_TURNOS_TB
WHERE ID_PERSONA = P_ID_PERSONA;
FETCH CURSOR_TURNOS INTO V_TOTAL;
CLOSE CURSOR_TURNOS;
RETURN V_TOTAL;
END FIDE_TOTAL_TURNOS_GUARDIA_FN;
 
