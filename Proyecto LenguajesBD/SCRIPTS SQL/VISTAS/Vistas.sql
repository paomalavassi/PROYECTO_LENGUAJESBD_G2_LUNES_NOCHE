CREATE MATERIALIZED VIEW FIDE_RESUMEN_FACTURACION_VM AS
SELECT
    P.ID_PERSONA AS ID_PERSONA,
    P.NOMBRE || ' ' || P.APELLIDO_PATERNO || ' ' || P.APELLIDO_MATERNO AS PERSONA,
    COUNT(F.ID_FACTURA) AS TOTAL_FACTURAS,
    COUNT(CASE WHEN TP.TIPO = 'MENSUAL' THEN 1 END) AS FACTURAS_MENSUAL,
    COUNT(CASE WHEN TP.TIPO = 'UNICO' THEN 1 END) AS FACTURAS_UNICO,
    FP.FORMA AS FORMA_PAGO_FRECUENTE
FROM FIDE_FACTURAS_TB F
JOIN FIDE_PERSONAS_TB P ON F.ID_PERSONA = P.ID_PERSONA
JOIN FIDE_TIPOS_PAGO_TB TP ON F.ID_TIPO_PAGO = TP.ID_TIPO_PAGO
JOIN FIDE_FORMAS_PAGOS_TB FP ON F.ID_FORMA_PAGO = FP.ID_FORMA_PAGO
GROUP BY
    P.ID_PERSONA,
    P.NOMBRE,
    P.APELLIDO_PATERNO,
    P.APELLIDO_MATERNO,
    FP.FORMA;

SELECT * FROM FIDE_RESUMEN_FACTURACION_VM;


CREATE MATERIALIZED VIEW FIDE_RESUMEN_SERVICIOS_VM AS
SELECT
    P.ID_PERSONA AS ID_PERSONA,
    P.NOMBRE || ' ' || P.APELLIDO_PATERNO || ' ' || P.APELLIDO_MATERNO AS TRABAJADOR,
    TS.TIPO_SERVICIO AS TIPO_SERVICIO,
    COUNT(S.ID_SERVICIO) AS TOTAL_SERVICIOS,
    MIN(S.FECHA_SALIDA) AS PRIMER_SERVICIO,
    MAX(S.FECHA_SALIDA) AS ULTIMO_SERVICIO
FROM FIDE_PERSONAS_SERVICIOS_TB PS
JOIN FIDE_PERSONAS_TB P ON PS.ID_PERSONA = P.ID_PERSONA
JOIN FIDE_SERVICIOS_TB S ON PS.ID_SERVICIO = S.ID_SERVICIO
JOIN FIDE_TIPOS_SERVICIOS_TB TS ON S.ID_TIPO_SERVICIO = TS.ID_TIPO_SERVICIO
GROUP BY
    P.ID_PERSONA,
    P.NOMBRE,
    P.APELLIDO_PATERNO,
    P.APELLIDO_MATERNO,
    TS.TIPO_SERVICIO
ORDER BY TOTAL_SERVICIOS DESC;

SELECT * FROM FIDE_RESUMEN_SERVICIOS_VM;