CREATE OR REPLACE VIEW FIDE_RESIDENTES_DETALLE_V AS
SELECT
    P.ID_PERSONA AS "ID Persona",
    P.NOMBRE || ' ' || P.APELLIDO_PATERNO || ' ' || P.APELLIDO_MATERNO AS "Residente",
    R.ID_RESIDENCIA AS "ID Residencia",
    R.MONTO_ALQUILER AS "Monto Alquiler",
    R.MONTO_MANTENIMIENTO AS "Monto Mantenimiento",
    TP.TIPO AS "Tipo de Pago",
    E.NOMBRE_ESTADO AS "Estado Residencia"
FROM FIDE_RESIDENTES_TB RS
JOIN FIDE_PERSONAS_TB P ON RS.ID_PERSONA = P.ID_PERSONA
JOIN FIDE_RESIDENCIAS_TB R ON RS.ID_RESIDENCIA = R.ID_RESIDENCIA
JOIN FIDE_TIPOS_PAGO_TB TP ON R.ID_TIPO_PAGO = TP.ID_TIPO_PAGO
JOIN FIDE_ESTADOS_TB E ON R.ID_ESTADO = E.ID_ESTADO
ORDER BY R.ID_RESIDENCIA, P.APELLIDO_PATERNO;

SELECT * FROM FIDE_RESIDENTES_DETALLE_V;


